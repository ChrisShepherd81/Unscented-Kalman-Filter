#include "PlotData.hpp"

///////////////////////////////////////////////////////////////////////////////////////
PlotData::PlotData(std::string title)
{
	this->title_ = title;
}
///////////////////////////////////////////////////////////////////////////////////////
void PlotData::addPoint(const Eigen::VectorXd& point)
{
	this->x_.push_back(point(0));
	this->y_.push_back(point(1));
}
///////////////////////////////////////////////////////////////////////////////////////
void PlotData::addPoint(double x, double y)
{
	this->x_.push_back(x);
	this->y_.push_back(y);
}
///////////////////////////////////////////////////////////////////////////////////////
auto PlotData::getAllX() const -> std::vector<double>
{
	return this->x_;
}
///////////////////////////////////////////////////////////////////////////////////////
auto PlotData::getAllY() const -> std::vector<double>
{
	return this->y_;
}
///////////////////////////////////////////////////////////////////////////////////////
std::string PlotData::getTitle() const
{
	return this->title_;
}
///////////////////////////////////////////////////////////////////////////////////////
bool PlotData::empty() const
{
  return this->x_.empty() || this->y_.empty();
}
